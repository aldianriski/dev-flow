#!/usr/bin/env node
/**
 * scan-legacy-docs.js — TASK-134b Phase 1 (Sprint 055c)
 *
 * Walks tracked *.md files; flags potential legacy/orphaned docs.
 * Per-file: incoming refs (git grep), age (git log), frontmatter status.
 * Flag: incoming_refs == 0 AND (age > 90d OR status:deprecated).
 *
 * Usage: node scripts/scan-legacy-docs.js [--threshold-days N] [--out path]
 * Default threshold: 90 days. Default output: docs/audit/legacy-doc-scan-<YYYY-MM-DD>.md
 */

'use strict';

const { execSync } = require('node:child_process');
const path = require('node:path');
const fs = require('node:fs');

const args = process.argv.slice(2);
const opt = (flag, dflt) => {
  const i = args.indexOf(flag);
  return i >= 0 && args[i + 1] ? args[i + 1] : dflt;
};

const THRESHOLD_DAYS = parseInt(opt('--threshold-days', '90'), 10);
// --date override: pass session date (YYYY-MM-DD) to avoid JS UTC vs local drift
// (Sprint 055b retro Friction #3 — JS new Date().toISOString() can drift one day from session date).
const today = opt('--date', new Date().toISOString().slice(0, 10));
const OUT_PATH = opt('--out', `docs/audit/legacy-doc-scan-${today}.md`);

// Excluded path prefixes (in addition to .gitignore handled by git ls-files)
const EXCLUDE_PREFIXES = [
  'docs/codemap/',                        // auto-generated by codemap-refresh hook
  'docs/audit/legacy-doc-scan-',          // self-output (re-runnable)
  '.claude/plugins/',                     // plugin cache
];

// Canonical repo-root *.md files (NOT flagged in repo-root anomaly section)
const CANONICAL_ROOT = new Set(['README.md', 'TODO.md', 'CONTRIBUTING.md', 'SECURITY.md', 'CHANGELOG.md', 'LICENSE.md']);

function sh(cmd) {
  try {
    return execSync(cmd, { encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'] }).trim();
  } catch (e) {
    return '';
  }
}

function listTrackedMarkdown() {
  const out = sh('git ls-files "*.md"');
  return out.split('\n')
    .map(p => p.trim())
    .filter(Boolean)
    .filter(p => !EXCLUDE_PREFIXES.some(pre => p.startsWith(pre)));
}

function getFrontmatter(filePath) {
  let raw = '';
  try {
    raw = fs.readFileSync(filePath, 'utf8');
  } catch {
    return { status: null, hasFrontmatter: false };
  }
  const m = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!m) return { status: null, hasFrontmatter: false };
  const fm = m[1];
  const sm = fm.match(/^status:\s*(\S+)/m);
  return { status: sm ? sm[1].toLowerCase() : null, hasFrontmatter: true };
}

function getAgeDays(filePath) {
  // %cs = committer date in short format YYYY-MM-DD
  const ds = sh(`git log -1 --format=%cs -- "${filePath}"`);
  if (!ds || !/^\d{4}-\d{2}-\d{2}$/.test(ds)) return null;
  const then = new Date(ds + 'T00:00:00Z').getTime();
  const now = new Date(today + 'T00:00:00Z').getTime();
  return Math.floor((now - then) / (1000 * 60 * 60 * 24));
}

function countIncomingRefs(filePath) {
  // Reference can match basename (filename.md) OR basename-stem (filename without .md)
  const basename = path.basename(filePath);
  const stem = basename.replace(/\.md$/, '');
  // Use literal-string grep (-F) for both basename + stem; union of matching files; subtract self.
  const matchSet = new Set();
  for (const needle of [basename, stem]) {
    if (!needle) continue;
    // -l = files-with-matches; -F = literal; -- '*.md' restricts file scope
    const out = sh(`git grep -lF "${needle}" -- "*.md"`);
    out.split('\n').forEach(p => {
      const trimmed = p.trim();
      if (trimmed && trimmed !== filePath) matchSet.add(trimmed);
    });
  }
  return matchSet.size;
}

function classify({ refs, ageDays, status }) {
  // Flag rule: refs == 0 AND (age > THRESHOLD OR status:deprecated)
  const noRefs = refs === 0;
  const oldEnough = ageDays != null && ageDays > THRESHOLD_DAYS;
  const deprecated = status === 'deprecated';

  if (!noRefs) return { flagged: false, verdict: 'KEEP', reason: 'in-use (incoming refs > 0)' };
  if (deprecated) return { flagged: true, verdict: 'DELETE', reason: 'no refs · status:deprecated' };
  if (oldEnough) {
    if (status == null) return { flagged: true, verdict: 'ARCHIVE', reason: `no refs · age ${ageDays}d · no frontmatter` };
    if (status === 'current') return { flagged: true, verdict: 'REVIEW', reason: `no refs · age ${ageDays}d · status:current (orphan-but-current)` };
    return { flagged: true, verdict: 'REVIEW', reason: `no refs · age ${ageDays}d · status:${status}` };
  }
  return { flagged: false, verdict: 'KEEP', reason: `no refs but age ${ageDays ?? 'unknown'}d below threshold` };
}

function detectSoftOrphans(rows) {
  // Soft orphan = refs > 0 BUT all incoming refs come from other rows that are also flagged or repo-root anomalies.
  // Build orphan-set first, then re-scan refs to detect cluster orphans.
  const orphanPaths = new Set(rows.filter(r => r.flagged || r.repoRootAnomaly).map(r => r.path));
  for (const r of rows) {
    if (r.flagged || r.repoRootAnomaly || r.refs === 0) continue;
    // Re-fetch ref source list (cheap second-pass via git grep)
    const basename = path.basename(r.path);
    const stem = basename.replace(/\.md$/, '');
    const sources = new Set();
    for (const needle of [basename, stem]) {
      const out = sh(`git grep -lF "${needle}" -- "*.md"`);
      out.split('\n').forEach(p => {
        const t = p.trim();
        if (t && t !== r.path) sources.add(t);
      });
    }
    const allSourcesAreOrphans = sources.size > 0 && [...sources].every(s => orphanPaths.has(s));
    if (allSourcesAreOrphans) {
      r.softOrphan = true;
      r.refSources = [...sources];
    }
  }
}

function renderReport(rows, scannedCount, threshold) {
  detectSoftOrphans(rows);

  const flagged = rows.filter(r => r.flagged);
  const repoRootAnomalies = rows.filter(r => r.repoRootAnomaly);
  const softOrphans = rows.filter(r => r.softOrphan);

  const counts = flagged.reduce((acc, r) => { acc[r.verdict] = (acc[r.verdict] || 0) + 1; return acc; }, {});
  const verdictLine = ['DELETE', 'ARCHIVE', 'REVIEW']
    .map(v => `${counts[v] || 0} ${v.toLowerCase()}`).join(' · ');

  const lines = [
    '---',
    'owner: Tech Lead (Aldian Rizki)',
    `last_updated: ${today}`,
    'update_trigger: Re-run scan',
    'status: current',
    'task: TASK-134b',
    'sprint: 055c',
    '---',
    '',
    `# Legacy-Doc Scan — ${today}`,
    '',
    `> Scope: tracked \`*.md\` files (excludes \`docs/codemap/\`, \`docs/audit/legacy-doc-scan-*\`, \`.claude/plugins/\`).`,
    `> Hard-flag rule: \`incoming_refs == 0 AND (age > ${threshold}d OR status:deprecated)\`.`,
    `> Repo-root anomaly: any \`*.md\` at repo root NOT in canonical set (README/TODO/CONTRIBUTING/SECURITY/CHANGELOG/LICENSE) — surfaced for manual review regardless of ref count.`,
    `> Soft-orphan: \`refs > 0\` BUT all incoming refs come from already-orphaned files (cluster orphan).`,
    '> Generated by: `node scripts/scan-legacy-docs.js`',
    '',
    '## Summary',
    '',
    `- Total files scanned: **${scannedCount}**`,
    `- Hard-flagged candidates: **${flagged.length}**`,
    `- Repo-root anomalies: **${repoRootAnomalies.length}**`,
    `- Soft orphans (cluster): **${softOrphans.length}**`,
    `- Hard-flagged verdict distribution: ${verdictLine}`,
    '',
    '## Hard-Flagged Candidates',
    '',
  ];

  if (flagged.length === 0) {
    lines.push('_No hard-flag candidates (refs==0 AND age/status condition). See Repo-Root Anomalies + Soft Orphans below._');
  } else {
    lines.push('| Path | Refs | Age (d) | Status | Verdict | Reason |');
    lines.push('|:-----|-----:|--------:|:-------|:--------|:-------|');
    for (const r of flagged) {
      const status = r.status ?? '_(none)_';
      const age = r.ageDays ?? '_unknown_';
      lines.push(`| \`${r.path}\` | ${r.refs} | ${age} | ${status} | **${r.verdict}** | ${r.reason} |`);
    }
  }

  lines.push('', '## Repo-Root Anomalies (manual review)', '');
  if (repoRootAnomalies.length === 0) {
    lines.push('_No repo-root anomalies. All `*.md` at repo root are canonical._');
  } else {
    lines.push('| Path | Refs | Age (d) | Status | Suggested Verdict |');
    lines.push('|:-----|-----:|--------:|:-------|:------------------|');
    for (const r of repoRootAnomalies) {
      const status = r.status ?? '_(none)_';
      const age = r.ageDays ?? '_unknown_';
      // Suggested verdict for repo-root anomaly: if status:deprecated → DELETE; if no refs → ARCHIVE; else REVIEW
      let suggest = 'REVIEW';
      if (r.status === 'deprecated') suggest = 'DELETE';
      else if (r.refs === 0) suggest = 'ARCHIVE';
      lines.push(`| \`${r.path}\` | ${r.refs} | ${age} | ${status} | **${suggest}** |`);
    }
  }

  lines.push('', '## Soft Orphans (cluster)', '');
  if (softOrphans.length === 0) {
    lines.push('_No cluster orphans. All referenced files have at least one ref from a non-orphaned source._');
  } else {
    lines.push('| Path | Refs | Age (d) | Ref Sources (all orphaned) |');
    lines.push('|:-----|-----:|--------:|:---------------------------|');
    for (const r of softOrphans) {
      const age = r.ageDays ?? '_unknown_';
      const srcs = r.refSources.map(s => `\`${s}\``).join(', ');
      lines.push(`| \`${r.path}\` | ${r.refs} | ${age} | ${srcs} |`);
    }
  }

  lines.push('', '## Verdict Legend', '');
  lines.push('- **DELETE** — `git rm`; preserve history in git log.');
  lines.push('- **ARCHIVE** — `git mv` to `docs/archive/<basename>`; add frontmatter pointer-back annotation.');
  lines.push('- **REVIEW** — `status:current` but anomaly location OR cluster-orphan; user judgment (re-link, re-scope, archive, or document orphan).');
  lines.push('- **KEEP** — has incoming refs from non-orphan source AND not anomaly-located; not flagged.');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const files = listTrackedMarkdown();
  if (files.length === 0) {
    console.error('No tracked *.md files found. Run from repo root.');
    process.exit(1);
  }

  console.log(`Scanning ${files.length} tracked *.md files (threshold: ${THRESHOLD_DAYS}d)...`);

  const rows = [];
  for (const filePath of files) {
    const refs = countIncomingRefs(filePath);
    const ageDays = getAgeDays(filePath);
    const fm = getFrontmatter(filePath);
    const verdict = classify({ refs, ageDays, status: fm.status });
    // Repo-root anomaly: file at repo root, not in canonical set
    const isRepoRoot = !filePath.includes('/') && !filePath.includes('\\');
    const repoRootAnomaly = isRepoRoot && !CANONICAL_ROOT.has(filePath);
    rows.push({ path: filePath, refs, ageDays, status: fm.status, repoRootAnomaly, ...verdict });
  }

  const report = renderReport(rows, files.length, THRESHOLD_DAYS);

  // Ensure output dir exists
  const outDir = path.dirname(OUT_PATH);
  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(OUT_PATH, report, 'utf8');

  const flaggedCount = rows.filter(r => r.flagged).length;
  const anomalyCount = rows.filter(r => r.repoRootAnomaly).length;
  const softOrphanCount = rows.filter(r => r.softOrphan).length;
  console.log(`Report: ${OUT_PATH}`);
  console.log(`Hard-flagged: ${flaggedCount} · repo-root anomalies: ${anomalyCount} · soft orphans: ${softOrphanCount} · total scanned: ${files.length}`);
}

main();
